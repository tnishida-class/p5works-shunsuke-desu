// テキスト「キーボード操作に反応する」
let x, y;

function setup(){
  createCanvas(windowWidth, windowHeight);
  x = width / 2;
  y = height / 2;
}

function draw(){
  background(5, 39, 94);
  ellipse(x, y, 30);
}

function mouseClicked(){
  x = mouseX;
  y = mouseY;
}

